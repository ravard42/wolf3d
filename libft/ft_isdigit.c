#include "libft.h"

int	ft_isdigit(int c)
{
	unsigned char	d;

	d = (unsigned char)c;
	if (c < 0 || c > 255)
		return (0);
	else if (d >= 48 && d <= 57)
		return (1);
	else
		return (0);
}
