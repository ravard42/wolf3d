#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	size_t	n;
	size_t	i;

	n = ft_strlen(s1);
	i = 0;
	while (s2[i] && i < len)
	{
		s1[n + i] = s2[i];
		i++;
	}
	s1[n + i] = '\0';
	return (s1);
}
