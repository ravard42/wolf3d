#include "libft.h"

char	*ft_rm_whitespace(char *str)
{
	int	i;

	i = 0;
	if (str)
	{
		while (str[i] == '\n' || str[i] == '\v' || str[i] == ' ' ||
			str[i] == '\t' || str[i] == '\r' || str[i] == '\f')
			i++;
		return (str + i);
	}
	return (NULL);
}
